class User(object):
    def __init__(self, name, age, gender, job, hobby):
        self.name   = name
        self.age    = age
        self.gender = gender
        self.job    = job
        self.hobby  = hobby

class Music(object):
    def __init__(self, title, album, group, bpm):
        self.title  = title
        self.album  = album
        self.group  = group
        self.bpm    = bpm
